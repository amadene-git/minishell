int		ft_strcmp(const char *s1, const char *s2)
{
	if (!s1 && !s2)
		return (0);
	if (!s1)
		return ((int)(-*s2));
	if (!s2)
		return ((int)*s1);
	while (*s1 && *s2 && !(*s1 - *s2))
	{
		s1++;
		s2++;
	}
	return ((int)(*s1 - *s2));
}
